// Write a program to print numbers from 1 to 10.
// using for, while & do while loop
public class whileDoWhile
{
    public static void main(String[] args) {

        System.out.println("While Do While:");
        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 10);
    }
}
